def dobro(n):
    """
    Função que retorna o dobro de um número.
    :param n: número a ser dobrado
    :return: dobro do número
    """
    res = n * 2 
    return res


def metade(n):
    res = n / 2
    return res


def aumentar(n):
    res = n *1.1
    return res


def diminuir(n):
    res = n * 0.9
    return res